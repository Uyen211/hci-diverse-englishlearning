import { cn } from "@/lib/utils"

function Card({
  className,
  variant = "default", // default | hero
  ...props
}) {
  return (
    <div
      data-slot="card"
      data-variant={variant}
      className={cn(
        "group/card flex flex-col gap-4 overflow-hidden border transition-all duration-300",
        variant === "hero"
          ? "bg-gradient-haze rounded-xxl border-primary/4 shadow-active-glow p-8"
          : "bg-card rounded-xl border-primary/4 shadow-glow hover:shadow-active-glow p-6",
        className
      )}
      {...props} />
  );
}

